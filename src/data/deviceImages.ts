export const getDeviceImage = (slug: string, category?: string): string | null => {
    let s = slug.toLowerCase();

    // Deduplicate brand if it appears twice (e.g. 'apple-apple-iphone' -> 'apple-iphone')
    const segments = s.split('-');
    if (segments.length > 1 && segments[0] === segments[1]) {
        s = segments.slice(1).join('-');
    }

    // 1. Exact Match (The "Elite" Image)
    if (DEVICE_IMAGES[s]) return DEVICE_IMAGES[s];

    // 2. Smart Brand Fallback (Detected by Keywords)
    if (s.includes('iphone') || s.includes('ipad') || s.includes('macbook') || s.includes('watch') || s.includes('apple')) {
        // Extra check for pro-max fallback to iphone brand if specific pro-max image missing
        if (s.includes('pro-max') && !DEVICE_IMAGES[s]) return DEVICE_IMAGES['apple'];
        return DEVICE_IMAGES['apple'];
    }
    if (s.includes('galaxy') || s.includes('samsung')) {
        return DEVICE_IMAGES['samsung'];
    }
    if (s.includes('pixel') || s.includes('google')) {
        return DEVICE_IMAGES['google'];
    }
    if (s.includes('xbox') || s.includes('microsoft')) {
        return DEVICE_IMAGES['xbox'];
    }
    if (s.includes('playstation') || s.includes('ps4') || s.includes('ps5') || s.includes('sony')) {
        return DEVICE_IMAGES['playstation'] || DEVICE_IMAGES['sony'];
    }
    if (s.includes('switch') || s.includes('nintendo') || s.includes('3ds')) {
        return DEVICE_IMAGES['nintendo'];
    }
    if (s.includes('huawei')) return DEVICE_IMAGES['huawei'];
    if (s.includes('xiaomi')) return DEVICE_IMAGES['xiaomi'];
    if (s.includes('oneplus')) return DEVICE_IMAGES['oneplus'];
    if (s.includes('oppo')) return DEVICE_IMAGES['oppo'];

    // 3. Simple Brand Extraction Fallback
    const firstWord = s.split('-')[0];
    if (DEVICE_IMAGES[firstWord]) return DEVICE_IMAGES[firstWord];

    // 4. Ultimate Fallback: The Belmobile Identity (SVG Icon instead of generic photo)
    // We return the Sony (PlayStation) logo for consoles as a fallback if brand is missing,
    // or the primary Apple logo for mixed categories, but preference is the favicon/brand logo.
    return '/favicon.svg';
};
